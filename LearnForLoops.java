package week1.day1;

public class LearnForLoops {

	public static void main(String[] args) {
		// want to print 1 to 5
	   //for --->used to run same code multiple times
		
		//installation: condition; incremental/decrement
		for (int i = 1; i <= 10; i++) { //print only odd numbers
			
			if (i % 2 == 1) { //i % 2 == 0 is for even numbers
			 System.out.println(i);  //(int i = 10; i >= 1; i--) reverse
		}

	}
	}
}
